import mongoose from "mongoose";

// Define the schema for the blog post
const postSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  content: {
    type: String,
    required: true,
  },
  author: {
    type: String,
    required: true,
  },
  image: {
    type: String, // Assuming the image will be stored as a URL
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  likes: {
    type: Number,
    default: 0,
  },
});

// Create the model from the schema
const Post = mongoose.model("Post", postSchema);

export default Post;
